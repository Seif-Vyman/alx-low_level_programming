#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - check the code
 * @a: number of arguments
 * @b: vector which have arguments
 * Return: sum of 2 numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - check the code
 * @a: number of argu
 * @b: number of argu
 * Return: sub of 2 numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - check the code
 * @a: number of argu
 * @b: number of argu
 * Return: mul of 2 numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - check the code
 * @a: number of argu
 * @b: number of argu
 * Return: div of 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - check the code
 * @a: number of argu
 * @b: number of argu
 * Return: mod of 2 numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
